    private void Showdata()
    {
        String[] tencot = {"Mã nhân viên", "Họ Tên", "Năm Sinh", "Địa Chỉ", "Số Điện Thoai"};
        tbModelnhanvien.setColumnIdentifiers(tencot);
        tbnhanvien.setModel(tbModelnhanvien);
        cleardata();
        nhanvienDAO nv = new nhanvienDAO();
        List<NhanVien> data = nv.getNhanVien();
        for (int i = 0; i < data.size(); i++) {
            String[] row = {
                String.valueOf(data.get(i).getManv()),
                data.get(i).getTennv(),
                String.valueOf(data.get(i).getNamsinh()),
                data.get(i).getDiachi(),
                data.get(i).getSodienthoai(),
               
            };
            tbModelnhanvien.addRow(row);
        }
    }

