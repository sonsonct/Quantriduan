    private void tbnhanvienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbnhanvienMouseClicked
        // TODO add your handling code here:
        int row = tbnhanvien.getSelectedRow();
        if(row >= 0)
        {
            txtma.setText(tbnhanvien.getValueAt(row, 0).toString());
            txtten.setText(tbnhanvien.getValueAt(row, 1).toString());
            String date = tbnhanvien.getValueAt(row, 2).toString();
                Date date1;
            try {
                date1 = new SimpleDateFormat("yyyy-MM-dd").parse(date);
                txtnamsinh.setDate(date1);
            } catch (ParseException ex) {
                Logger.getLogger(QuanLyNhanVien.class.getName()).log(Level.SEVERE, null, ex);
            }
            txtdiachi.setText(tbnhanvien.getValueAt(row, 3).toString());
            txtsdt.setText(tbnhanvien.getValueAt(row, 4).toString());
        }
    }//GEN-LAST:event_tbnhanvienMouseClicked

